#include "../../include/minirt.h"

t_vec3	vec_cross(t_vec3 a, t_vec3 b)
{
	t_vec3	v;

	v.x = a.y * b.z - a.z * b.y;
	v.y = a.x * b.z - a.z * b.x;
	v.z = a.x * b.y - a.y * b.x;
	return (v);
}
